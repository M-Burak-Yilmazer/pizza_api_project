const { mongoose } = require("../configs/dbConnection");
/* ------------------------------------------------------- */

const toppingsSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
      unique: true,
    },
  },
  {
    collection: "toppings",
    timestamps: true,
  }
);

module.exports = mongoose.model("Topping", toppingsSchema);
